// Generally you will know when to use useReducer()
// when useState() becomes too cumbersome

// useState()
// is your main state managment tool
// for independent pieces of state/data
// great if state updates are easy
// and limited to a few kinds of updates

// useReducer()
// great if you need more power
// should be considered if you have related pieces of state/data
// if you have more complex state updates
